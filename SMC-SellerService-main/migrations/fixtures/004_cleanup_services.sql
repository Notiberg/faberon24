-- ==========================================
-- Cleanup duplicate and unwanted services
-- ==========================================

-- Удаляем дублирующиеся и ненужные услуги
DELETE FROM services WHERE company_id = 1 AND id IN (
    1,      -- Комплексная мойка (старая)
    2,      -- Экспресс-мойка (старая)
    3,      -- Детейлинг (старая)
    10,     -- Замена масла
    110,    -- Обработка внешней поверхности стекол
    113,    -- Очистка пластиковых и кожаных поверхностей
    114     -- Мойка двигателя
);

-- Обновляем последовательность ID
SELECT setval('services_id_seq', (SELECT MAX(id) FROM services));
